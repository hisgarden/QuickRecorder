import SwiftUI

extension Color {
    /// Custom purple color from Assets
    static var mypurple: Color {
        Color("mypurple")
    }
    
    /// Custom blue color from Assets
    static var myblue: Color {
        Color("myblue")
    }
    
    /// Custom green color from Assets
    static var mygreen: Color {
        Color("mygreen")
    }
    
    /// Custom red button color from Assets
    static var buttonRed: Color {
        Color("buttonRed")
    }
    
    /// Custom dark red button color from Assets
    static var buttonRedDark: Color {
        Color("buttonRedDark")
    }
    
    /// Custom dark red color from Assets
    static var darkMyRed: Color {
        Color("dark_my_red")
    }
}

